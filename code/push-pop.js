var numbers = [78, 85, 45, 65, 36];
// push will add a number in last
numbers.push(29);
console.log(numbers);

// pop will remove the last number
var friends = ['balu', 'kalu', 'dalu']
friends.pop('dalu');
console.log(friends);

// shift() and unshift() will add/remove in the beginning.

var fruits = ['apple', 'orange', 'mango']
fruits.unshift('jack');
console.log(fruits);